// A filled disc with the icon shape knocked out of its centre, so a row of
// these reads as uniform circular buttons. The icon is scaled to half size
// and centred, leaving generous padding around it.
export function CircleIcon({
  path,
  color = 'white',
  size = 24,
}: {
  path: string;
  color?: string;
  size?: number;
}) {
  const maskId = useId();
  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      viewBox="0 0 24 24"
      width={size}
      height={size}
    >
      <mask id={maskId}>
        <circle cx="12" cy="12" r="12" fill="white" />
        {/* scale(0.5) → icon is 2x smaller; translate(6,6) recentres it. */}
        <g transform="translate(6 6) scale(0.5)">
          <path d={path} fill="black" fillRule="evenodd" />
        </g>
      </mask>
      <circle cx="12" cy="12" r="12" fill={color} mask={`url(#${maskId})`} />
    </svg>
  );
}

